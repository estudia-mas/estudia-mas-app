function credito(monto: number, plazo: number, tasa: number, inicio: string) {
  const mensualidad = Math.round(mensualidadFrancesa(monto, plazo, tasa))
  return {
    montoTotal: monto,
    plazo,
    tasaAnual: tasa,
    saldoActual: monto,
    mensualidad,
    mensualidadBase: mensualidad,
    fechaInicio: inicio,
    recompensaAplicada: false,
  }
}

